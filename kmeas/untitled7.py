import pandas as pd
import matplotlib.pyplot as plt


def grafico_clusters():
    df = pd.read_csv("clusters1.csv")
    plt.figure(figsize=(8,6))
    plt.scatter(df['x'], df['y'], c=df['cluster'], cmap='tab10', s=10)
    plt.scatter(df['centro_x'], df['centro_y'], c='black', marker='*', s=120, label='Centroides')
    plt.title("Visualización de Clusters (K-Means con KD-Tree)")
    plt.xlabel("X")
    plt.ylabel("Y")
    plt.legend()
    plt.grid(True)
    plt.show()

if __name__ == "__main__":

    grafico_clusters()

def grafico_tiempo_vs_puntos():
    df = pd.read_csv("tiempos_1.csv")

    plt.figure(figsize=(8,6))

    for version in df['version'].unique():
        sub = df[df['version'] == version]
        plt.plot(sub['n_puntos'], sub['tiempo_ms'], marker='o', linewidth=2, label=version)

    plt.title("K-Means Fuerza Bruta vs KD-Tree - k = 5")
    plt.xlabel("Número de puntos (n)")
    plt.ylabel("Tiempo de ejecución (ms)")
    plt.legend(title="Versión", loc="upper left")
    plt.grid(True, linestyle="--", alpha=0.6)
    plt.tight_layout()
    plt.show()

if __name__ == "__main__":
    grafico_tiempo_vs_puntos()

