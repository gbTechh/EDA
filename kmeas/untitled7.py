import pandas as pd
import matplotlib.pyplot as plt

def grafico_tiempo_vs_iteracion():
    # Leer el CSV (tolera comas y puntos y comas)
    df = pd.read_csv("B_KDTree.csv", sep=r'[,;]+', engine='python')

    # Limpiar los nombres de columnas
    df.columns = df.columns.str.strip()

    print("Columnas detectadas:", df.columns.tolist())

    plt.figure(figsize=(8,6))

    for version in df['version'].unique():
        sub = df[df['version'] == version]
        plt.plot(sub['ite'], sub['tiempo_ms'], marker='o', linewidth=2, label=version)

    plt.title("Tiempo de ejecución por iteración")
    plt.xlabel("Iteración")
    plt.ylabel("Tiempo (ms)")
    plt.legend(title="Versión", loc="upper left")
    plt.grid(True, linestyle="--", alpha=0.6)
    plt.tight_layout()
    plt.show()

if __name__ == "__main__":
    grafico_tiempo_vs_iteracion()